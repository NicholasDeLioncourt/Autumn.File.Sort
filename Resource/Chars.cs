namespace Autumn.File
{
    using System.Runtime.InteropServices;
    //=============================================================================================
    /// <summary>Common Char literals.</summary>
    //=====================================================================================LNDL====
    [StructLayout(LayoutKind.Sequential)]
    public struct Chars
    {
        /// <summary>Ampersand character</summary>
        public const char CharAmpersand = '&';
        /// <summary>Apostrophe character</summary>
        public const char CharApostrophe = '\'';
        /// <summary>[@] character</summary>
        public const char CharAsterisk = '*';
        /// <summary>At character: @</summary>
        public const char CharAt = '@';
        /// <summary>[\] character</summary>
        public const char CharBackslash = '\\';
        /// <summary>[{] character</summary>
        public const char CharBraceLeft = '{';
        /// <summary>[}] character</summary>
        public const char CharBraceRight = '}';
        /// <summary>[¦] typography character</summary>
        public const char CharBrokenBar = '¦';
        /// <summary>Bullet typography character</summary>
        public const char CharBullet ='•';
        /// <summary>Typography caret: ^</summary>
        public const char CharCaret = '^';
        /// <summary>[¢] character</summary>
        public const char CharCents = '¢';
        /// <summary>[:] character</summary>
        public const char CharColon = ':';
        /// <summary>[,] character</summary> 
        public const char CharComma = ',';
        /// <summary>[©] character</summary> 
        public const char CharCopyright = '©';
        /// <summary>The dash, or minus sign, character [-]</summary>
        public const char CharDash = '-';
        /// <summary>The degree character [°]</summary>
        public const char CharDegree = '°';
        /// <summary>[$] character</summary>
        public const char CharDollar = '$';
        /// <summary>[…] character</summary>
        public const char CharEllipsis = '…';
        /// <summary>[!] character</summary>
        public const char CharExclamation = '!';
        /// <summary>[¡] character</summary>
        public const char CharExclamationInverted = '¡';
        /// <summary>[=] character</summary>
        public const char CharEquals = '=';
        /// <summary>[/] character</summary>
        public const char CharForwardslash = '/';
        /// <summary>‐ character</summary>
        public const char CharHyphen = '‐';
        /// <summary>[ character</summary>
        public const char CharLeftBrace = '[';
        /// <summary>[‽] typography character, often represented by ?! or !?</summary>
        public const char CharInterrobange = '‽';
        /// <summary>[◊] character, often referred to as a diamond</summary>
        public const char CharLosenge = '◊';
        /// <summary>[‖] typography character</summary>
        public const char CharMagnitude = '‖';
        /// <summary>[\0] character</summary>
        public const char CharNull = '\0';
        /// <summary>[÷] character</summary>
        public const char CharObelus = '÷';
        /// <summary>[#] character</summary>
        public const char CharOctothorpe = '#';
        /// <summary>[1] character</summary>
        public const char CharOne = '1';
        /// <summary>[%] character</summary>
        public const char CharPercent = '%';
        /// <summary>[|] character</summary>
        public const char CharPipe = '|';
        /// <summary>[+] character</summary>
        public const char CharPlus = '+';
        /// <summary>[.] character</summary>
        public const char CharPeriod = '.';
        /// <summary>[?] character</summary>
        public const char CharQuestion = '?';
        /// <summary>[®] character</summary>
        public const char CharRegisteredTrademark = '®';
        /// <summary>] character</summary>
        public const char CharRightBrace = ']';
        /// <summary>[§] character</summary>
        public const char CharSection = '§';
        /// <summary>[;] character</summary>
        public const char CharSemicolon = ';';
        /// <summary>Space character</summary>
        public const char CharSpace = ' ';
        /// <summary>Tab character</summary>
        public const char CharTab = '\t';
        /// <summary>[~] character</summary>
        public const char CharTilde = '~';
        /// <summary>[™] character</summary>
        public const char CharTrademark = '™';
        /// <summary>[_] character</summary>
        public const char CharUnderscore = '_';
        /// <summary>[0] character</summary>
        public const char CharZero = '0';
    }
}
